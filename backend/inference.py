def detect_fraud(msg: str, post_id: str):
    if not msg:
        return

    with Connection(SQL_DB_FILE) as conn:
        curr = conn.cursor()

        for url in re.findall(URL_REGEX, msg):
            if not url.startswith("http"):
                url = "http://" + url

            domain = urlparse(url).netloc.split(".")
            if domain[0] in ("ww", "www"):
                domain = domain[1:]
            domain = ".".join(domain)

            curr.execute("""SELECT COUNT(*) FROM flagged_links WHERE url LIKE ?""",
                         (f"%{domain}%",))
            if curr.fetchone()[0] > 0:
                curr.execute(f"""UPDATE posts SET fraud_detected=1, fraud_type=1 WHERE id = {int(post_id)}""")
                conn.commit()
                return

        result = model.predict(vectorizer.transform([msg]))[0]
        fraud_type = 2 if result == 1 else 0
        curr.execute(f"""UPDATE posts SET fraud_detected=?, fraud_type=? WHERE id = ?""",
                     (int(result), fraud_type, int(post_id)))
        conn.commit()
